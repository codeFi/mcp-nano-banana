interface GeminiResponse {
  candidates?: Array<{
    content?: {
      parts?: Array<{
        text?: string;
        inlineData?: {
          mimeType: string;
          data: string;
        };
      }>;
    };
  }>;
  error?: {
    message: string;
  };
}

interface InputImage {
  data: string;
  mimeType: string;
}

interface FaviconOptions {
  appName?: string;
  shortName?: string;
  themeColor?: string;
  backgroundColor?: string;
  includeReadme?: boolean;
}

const FAVICON_SIZES = [
  { size: 16, name: "favicon-16x16.png" },
  { size: 32, name: "favicon-32x32.png" },
  { size: 180, name: "apple-touch-icon.png" },
  { size: 192, name: "android-chrome-192x192.png" },
  { size: 512, name: "android-chrome-512x512.png" },
];
const ICO_SIZES = [16, 32, 48];

function getMimeType(filePath: string): string {
  const ext = extname(filePath).toLowerCase();
  const mimeTypes: Record<string, string> = {
    '.png': 'image/png',
    '.jpg': 'image/jpeg',
    '.jpeg': 'image/jpeg',
    '.webp': 'image/webp',
    '.gif': 'image/gif',
  };
  return mimeTypes[ext] || 'image/png';
}

function getHtmlSnippet(): string {
  return `<link rel="icon" href="/favicon.ico" sizes="any">
<link rel="apple-touch-icon" sizes="180x180" href="/apple-touch-icon.png">
<link rel="icon" type="image/png" sizes="32x32" href="/favicon-32x32.png">
<link rel="icon" type="image/png" sizes="16x16" href="/favicon-16x16.png">
<link rel="manifest" href="/site.webmanifest">`;
}

function createManifestContent(options: FaviconOptions): string {
  const appName = options.appName || "My App";
  const shortName = options.shortName || "App";
  const themeColor = options.themeColor || "#ffffff";
  const backgroundColor = options.backgroundColor || "#ffffff";

  const manifest = {
    name: appName,
    short_name: shortName,
    icons: [
      {
        src: "/android-chrome-192x192.png",
        sizes: "192x192",
        type: "image/png",
      },
      {
        src: "/android-chrome-512x512.png",
        sizes: "512x512",
        type: "image/png",
      },
    ],
    theme_color: themeColor,
    background_color: backgroundColor,
    display: "standalone",
  };

  return JSON.stringify(manifest, null, 2);
}

function createReadmeContent(): string {
  return `Favicon Package Generated by mcp-nano-banana

Your package includes:
- favicon.ico: Multi-size icon for legacy browsers.
- favicon-16x16.png: For browser tabs.
- favicon-32x32.png: For browser tabs (higher resolution).
- apple-touch-icon.png: For iOS home screens.
- android-chrome-192x192.png: For Android devices.
- android-chrome-512x512.png: For Android devices (higher resolution).
- site.webmanifest: For PWA and modern browser integration.

How to use:
1. Place all files in the root directory of your website.
2. Add the following lines to the <head> of your HTML files:

${getHtmlSnippet()}
`;
}

async function loadImageFromPath(filePath: string): Promise<InputImage> {
  const buffer = await readFile(filePath);
  const base64 = buffer.toString('base64');
  const mimeType = getMimeType(filePath);
  return { data: base64, mimeType };
}

async function buildFaviconFiles(
  baseImage: Buffer,
  options: FaviconOptions
): Promise<Map<string, Buffer>> {
  const files = new Map<string, Buffer>();

  const icoPngBuffers = await Promise.all(
    ICO_SIZES.map((size) =>
      sharp(baseImage)
        .resize(size, size, { fit: "fill" })
        .png()
        .toBuffer()
    )
  );
  const icoBuffer = await pngToIco(icoPngBuffers);
  files.set("favicon.ico", icoBuffer);

  for (const { size, name } of FAVICON_SIZES) {
    const resized = await sharp(baseImage)
      .resize(size, size, { fit: "fill" })
      .png()
      .toBuffer();
    files.set(name, resized);
  }

  const manifestContent = createManifestContent(options);
  files.set("site.webmanifest", Buffer.from(manifestContent, "utf8"));

  if (options.includeReadme !== false) {
    files.set("README.md", Buffer.from(createReadmeContent(), "utf8"));
  }

  return files;
}

async function writeFaviconFiles(
  outputDir: string,
  files: Map<string, Buffer>
): Promise<void> {
  await mkdir(outputDir, { recursive: true });
  const writes = Array.from(files.entries()).map(([name, data]) =>
    writeFile(join(outputDir, name), data)
  );
  await Promise.all(writes);
}

async function createFaviconZip(files: Map<string, Buffer>): Promise<Buffer> {
  const zip = new JSZip();
  for (const [name, data] of files.entries()) {
    zip.file(name, data);
  }
  return zip.generateAsync({ type: "nodebuffer" });
}

function buildIconPrompt(userPrompt: string): string {
  return `A professional, modern, square app icon. Vector art style. Centered object. The icon should represent: "${userPrompt}". The background should follow the user's description. The icon must be clear, simple, and easily recognizable at small sizes. No text unless specified.`;
}

