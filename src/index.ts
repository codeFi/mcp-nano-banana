export async function generateImage(
  prompt: string,
  images: InputImage[] = [],
  model: string = "gemini-3-pro-image-preview",
  aspectRatio: string = "1:1",
  resolution: string = "1K",
  outputFormat: string = "png"
): Promise<{ text?: string; imageData?: string; mimeType?: string; modelUsed?: string; fallback?: boolean }> {
  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) {
    throw new Error("GEMINI_API_KEY environment variable is not set");
  }

  // Build parts array: text prompt first, then any images
  const parts: Array<{ text: string } | { inlineData: { mimeType: string; data: string } }> = [
    { text: prompt }
  ];

  for (const image of images) {
    parts.push({
      inlineData: {
        mimeType: image.mimeType || "image/png",
        data: image.data,
      },
    });
  }

  const makeRequest = async (currentModel: string) => {
    return fetch(
      `${GEMINI_API_URL}/${currentModel}:generateContent`,
      {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "x-goog-api-key": apiKey,
        },
        body: JSON.stringify({
          contents: [{ parts }],
          generationConfig: {
            responseModalities: ["TEXT", "IMAGE"],
            imageConfig: {
              aspectRatio: aspectRatio,
              imageSize: resolution,
            },
          },
        }),
      }
    );
  };

  let response = await makeRequest(model);
  let usedModel = model;
  let didFallback = false;

  // If Pro model fails with quota/rate limit, fallback to Flash
  if (!response.ok && model === "gemini-3-pro-image-preview") {
    const status = response.status;
    if (status === 429 || status === 403 || status === 402) {
      usedModel = "gemini-2.5-flash-image";
      didFallback = true;
      response = await makeRequest(usedModel);
    }
  }

  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(`Gemini API error: ${response.status} - ${errorText}`);
  }

  const data: GeminiResponse = await response.json();

  if (data.error) {
    throw new Error(`Gemini API error: ${data.error.message}`);
  }

  const responseParts = data.candidates?.[0]?.content?.parts;
  if (!responseParts || responseParts.length === 0) {
    throw new Error("No content generated");
  }

  const result: { text?: string; imageData?: string; mimeType?: string; modelUsed?: string; fallback?: boolean } = {
    modelUsed: usedModel,
    fallback: didFallback,
  };

  for (const part of responseParts) {
    if (part.text) {
      result.text = part.text;
    }
    if (part.inlineData) {
      result.imageData = part.inlineData.data;
      result.mimeType = part.inlineData.mimeType;
    }
  }

  return result;
}


const server = new Server(
  {
    name: "mcp-nano-banana",
    version: "1.0.0",
  },
  {
    capabilities: {
      tools: {},
    },
  }
);

server.setRequestHandler(ListToolsRequestSchema, async () => {
  return { tools };
});

server.setRequestHandler(CallToolRequestSchema, async (request) => {
  const { name, arguments: args } = request.params;

  try {
    if (name === "generate_image") {
      const { prompt, imagePaths, model, aspectRatio, resolution, outputFormat, outputPath } = args as {
        prompt: string;
        imagePaths?: string[];
        model?: string;
        aspectRatio?: string;
        resolution?: string;
        outputFormat?: string;
        outputPath?: string;
      };

      // Load images from disk paths
      const images: InputImage[] = [];
      if (imagePaths && imagePaths.length > 0) {
        for (const path of imagePaths) {
          const image = await loadImageFromPath(path);
          images.push(image);
        }
      }

      const result = await generateImage(
        prompt,
        images,
        model,
        aspectRatio,
        resolution,
        outputFormat
      );

      const content: Array<{ type: string; text?: string; data?: string; mimeType?: string }> = [];

      // Report model used and fallback status
      let statusText = `Model used: ${result.modelUsed}`;
      if (result.fallback) {
        statusText += ` (fallback from gemini-3-pro-image-preview due to quota/rate limit)`;
      }

      if (result.text) {
        statusText += `\n\n${result.text}`;
      }

      content.push({ type: "text", text: statusText });

      if (result.imageData && result.mimeType) {
        // Save to file if outputPath provided
        if (outputPath) {
          await writeFile(outputPath, Buffer.from(result.imageData, 'base64'));
          content.push({ type: "text", text: `Image saved to: ${outputPath}` });
        } else {
          content.push({
            type: "image",
            data: result.imageData,
            mimeType: result.mimeType,
          });
        }
      }

      return { content };
    }

    if (name === "generate_favicons") {
      const {
        prompt,
        imagePath,
        model,
        outputDir,
        outputPath,
        returnZipBase64,
        appName,
        shortName,
        themeColor,
        backgroundColor,
        includeReadme,
      } = args as {
        prompt?: string;
        imagePath?: string;
        model?: string;
        outputDir?: string;
        outputPath?: string;
        returnZipBase64?: boolean;
        appName?: string;
        shortName?: string;
        themeColor?: string;
        backgroundColor?: string;
        includeReadme?: boolean;
      };

      if (!prompt && !imagePath) {
        throw new Error("Either prompt or imagePath must be provided");
      }

      if (!outputDir && !outputPath && !returnZipBase64) {
        throw new Error("Provide outputDir, outputPath, or set returnZipBase64 to true");
      }

      let baseImageBuffer: Buffer;

      if (imagePath) {
        const loaded = await loadImageFromPath(imagePath);
        baseImageBuffer = Buffer.from(loaded.data, "base64");
      } else {
        const iconPrompt = buildIconPrompt(prompt || "");
        const result = await generateImage(
          iconPrompt,
          [],
          model,
          "1:1",
          "png"
        );

        if (!result.imageData) {
          throw new Error("No image generated for favicon package");
        }

        baseImageBuffer = Buffer.from(result.imageData, "base64");
      }

      const options: FaviconOptions = {
        appName,
        shortName,
        themeColor,
        backgroundColor,
        includeReadme,
      };

      const files = await buildFaviconFiles(baseImageBuffer, options);

      if (outputDir) {
        await writeFaviconFiles(outputDir, files);
      }

      let zipBuffer: Buffer | undefined;
      if (outputPath || returnZipBase64) {
        zipBuffer = await createFaviconZip(files);
      }

      const content: Array<{ type: string; text?: string; data?: string; mimeType?: string }> = [];
      content.push({
        type: "text",
        text: `Favicon package ready.\n\nHTML snippet:\n${getHtmlSnippet()}`,
      });

      if (outputDir) {
        content.push({ type: "text", text: `Files written to: ${outputDir}` });
      }

      if (outputPath && zipBuffer) {
        await writeFile(outputPath, zipBuffer);
        content.push({ type: "text", text: `ZIP saved to: ${outputPath}` });
      } else if (returnZipBase64 && zipBuffer) {
        content.push({
          type: "text",
          text: `ZIP (base64): ${zipBuffer.toString("base64")}`,
        });
      }

      return { content };
    }

    throw new Error(`Unknown tool: ${name}`);
  } catch (error) {
    const errorMessage = error instanceof Error ? error.message : String(error);
    return {
      content: [{ type: "text", text: `Error: ${errorMessage}` }],
      isError: true,
    };
  }
});

async function main() {
  const transport = new StdioServerTransport();
  await server.connect(transport);
  console.error("Nano Banana MCP server running on stdio");
}

main().catch(console.error);
